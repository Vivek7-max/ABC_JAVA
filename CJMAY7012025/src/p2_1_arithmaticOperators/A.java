package p2_1_arithmaticOperators;
class A 
{
	public static void main(String[] args) 
	{
		System.out.println(4+5); //9
		System.out.println("Arun" + 50); //Arun50
		System.out.println(50 + "Bharati"); //50Bharati
		System.out.println("Arun" + "Bharati"); //ArunBharati
		System.out.println(10+20+"Sheela"); //30Sheela
		System.out.println("Arun" + 10 + 20 +"Bharati"); //Arun1020Bharati
		System.out.println(10+50+"Sheela"+40); //60Sheela40
		System.out.println('A'+1);  //66
		System.out.println('a'+'z'); //219
		System.out.println('A'+"A"); //AA
		System.out.println(10+20+'A'+"A"+50); //95A50
		//System.out.println(10+true); //CTE
		//System.out.println(false+true); //CTE
	}
}
