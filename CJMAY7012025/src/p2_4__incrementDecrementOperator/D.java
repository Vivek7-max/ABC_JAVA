package p2_4__incrementDecrementOperator;

class  D
{
	public static void main(String[] args) 
	{
		int a = 10;
		
		int b = ++a; //a = 11 //b = 11
		
		System.out.println(++a); //12 //a=12
		
		System.out.println(++b); //12 //b=12
		
		System.out.println(a); //12
		System.out.println(b); //12
	}
}
